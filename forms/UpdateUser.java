    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
String email = txtEmail.getText().trim();

        if (email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "❌ Email is required to update user.");
            return;
        }

        String name = txtName.getText().trim();
        String gender = radioMale.isSelected() ? "Male" :
                        (radioFemale.isSelected() ? "Female" : "");
        String contact = txtContact.getText().trim();
        String address = txtAddress.getText().trim();
        String state = txtState.getText().trim();
        String country = txtCountry.getText().trim();

        String finalImageName = oldImageName;

        // SAVE NEW IMAGE IF SELECTED
        if (selectedImageFile != null) {
            try {
                String safeEmail = email.replaceAll("[^a-zA-Z0-9]", "_");
                String extension = selectedImageFile.getName()
                                    .substring(selectedImageFile.getName().lastIndexOf(".") + 1);

                finalImageName = safeEmail + "." + extension;

                File folder = new File("src/images/");
                if (!folder.exists()) folder.mkdirs();

                File dest = new File(folder, finalImageName);

                ImageIO.write(ImageIO.read(selectedImageFile), extension, dest);

            } catch (IOException ex) {
                JOptionPane.showMessageDialog(this, "❌ Failed to save image.");
                return;
            }
}

try {
            Connection con = ConnectionProvider.getCon();
            PreparedStatement ps = con.prepareStatement(
                "UPDATE userdetails SET name=?, gender=?, contact=?, address=?, state=?, country=?, imagename=? WHERE email=?"
            );

            ps.setString(1, name);
            ps.setString(2, gender);
            ps.setString(3, contact);
            ps.setString(4, address);
            ps.setString(5, state);
            ps.setString(6, country);
            ps.setString(7, finalImageName);
            ps.setString(8, email);

            int updated = ps.executeUpdate();

            if (updated > 0) {
                JOptionPane.showMessageDialog(this, "✅ User updated successfully.");
                clearForm();
            } else {
                JOptionPane.showMessageDialog(this, "❌ No user found with that email.");
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "❌ SQL Error:\n" + e.getMessage());
        }
    

    }//GEN-LAST:event_jButton3ActionPerformed

