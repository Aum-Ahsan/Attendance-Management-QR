    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
String email = txtEmail.getText().trim().toLowerCase();

        if (email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "❌ Please enter a Name or Email to search.");
            return;
        }

        try {
            Connection con = ConnectionProvider.getCon();
            PreparedStatement ps = con.prepareStatement(
                "SELECT * FROM userdetails WHERE LOWER(email)=? OR LOWER(name)=?"
            );
            ps.setString(1, email);
            ps.setString(2, email);

            ResultSet rs = ps.executeQuery();

            if (rs.next()) {
                txtName.setText(rs.getString("name"));
                txtEmail.setText(rs.getString("email"));

                String gender = rs.getString("gender");
                radioMale.setSelected("Male".equalsIgnoreCase(gender));
                radioFemale.setSelected("Female".equalsIgnoreCase(gender));

                txtContact.setText(rs.getString("contact"));
                txtAddress.setText(rs.getString("address"));
                txtState.setText(rs.getString("state"));
                txtCountry.setText(rs.getString("country"));

                oldImageName = rs.getString("imagename");

                if (oldImageName != null && !oldImageName.isEmpty()) {
                    String imgPath = "src/images/" + oldImageName;
                    File imgFile = new File(imgPath);

                    if (imgFile.exists()) {
                        ImageIcon icon = new ImageIcon(imgPath);
                        Image img = icon.getImage().getScaledInstance(322, 286, Image.SCALE_SMOOTH);
                        lblimage.setIcon(new ImageIcon(img));
                    }
                }
            } else {
                JOptionPane.showMessageDialog(this, "❌ User not found.");
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "SQL Error: " + e.getMessage());
        }
        // TODO add your handling code here:
        // REGISTER button clicked
    }//GEN-LAST:event_jButton2ActionPerformed


    private void lblimageMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblimageMouseClicked
        JFileChooser fc = new JFileChooser();
        if (fc.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            selectedImageFile = fc.getSelectedFile();

            try {
                BufferedImage img = ImageIO.read(selectedImageFile);
                Image dimg = img.getScaledInstance(322, 286, Image.SCALE_SMOOTH);
                lblimage.setIcon(new ImageIcon(dimg));
            } catch (IOException e) {
                JOptionPane.showMessageDialog(this, "❌ Cannot load image.");
            }
        }
    }//GEN-LAST:event_lblimageMouseClicked

