    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
  

    try {
        if (currentQRImage == null) {
            JOptionPane.showMessageDialog(this, "⚠️ No QR to save. Please select a user first.");
            return;
        }

        javax.swing.JFileChooser fileChooser = new javax.swing.JFileChooser();
        fileChooser.setDialogTitle("Save QR Code");
        fileChooser.setSelectedFile(new File(selectedEmail.replaceAll("[^a-zA-Z0-9]", "_") + ".png"));

        int userSelection = fileChooser.showSaveDialog(this);
        if (userSelection == javax.swing.JFileChooser.APPROVE_OPTION) {
            File fileToSave = fileChooser.getSelectedFile();
            ImageIO.write(currentQRImage, "png", fileToSave);
            JOptionPane.showMessageDialog(this, "✅ QR Code saved to: " + fileToSave.getAbsolutePath());
        }

    } catch (IOException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "❌ Error saving QR:\n" + ex.getMessage());
    }


      // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
                                        

    try {
        if (currentQRImage == null || selectedEmail == null) {
            JOptionPane.showMessageDialog(this, "⚠️ No QR to save. Please select a user first.");
            return;
        }

        String fileName = selectedEmail.replaceAll("[^a-zA-Z0-9]", "_") + ".png";
        File output = new File("src/qrcodes/" + fileName);
        output.getParentFile().mkdirs(); // Create folder if not exists

        if (output.exists()) {
            JOptionPane.showMessageDialog(this, "⚠️ QR Code already saved for this user.");
            return;
        }

        ImageIO.write(currentQRImage, "png", output);
        JOptionPane.showMessageDialog(this, "✅ QR Code saved to: src/qrcodes/" + fileName);

    } catch (IOException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "❌ Error saving QR:\n" + ex.getMessage());
    }



    }//GEN-LAST:event_jButton2ActionPerformed

