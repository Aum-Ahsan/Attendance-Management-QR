    private void userTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_userTableMouseClicked


    try {
        int index = userTable.getSelectedRow();
        if (index == -1) {
            lblimage.setIcon(null);
            return;
        }

        TableModel model = userTable.getModel();
        selectedName = model.getValueAt(index, 1).toString().trim();
        selectedEmail = model.getValueAt(index, 3).toString().trim();

        String qrData = "Name: " + selectedName + "\nEmail: " + selectedEmail;

        int size = 300;
        QRCodeWriter qrCodeWriter = new QRCodeWriter();
        BitMatrix bitMatrix = qrCodeWriter.encode(qrData, BarcodeFormat.QR_CODE, size, size);
        currentQRImage = MatrixToImageWriter.toBufferedImage(bitMatrix); // Save to variable

        Image scaled = currentQRImage.getScaledInstance(lblimage.getWidth(), lblimage.getHeight(), Image.SCALE_SMOOTH);
        lblimage.setIcon(new ImageIcon(scaled));

    } catch (Exception ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "❌ Error displaying QR:\n" + ex.getMessage());
    }


    }//GEN-LAST:event_userTableMouseClicked

    
  private void generateQRCode(String data, String filePath) throws WriterException, IOException {
    int size = 300;
    QRCodeWriter qrCodeWriter = new QRCodeWriter();
    BitMatrix bitMatrix = qrCodeWriter.encode(data, BarcodeFormat.QR_CODE, size, size);
    File qrFile = new File(filePath);
    qrFile.getParentFile().mkdirs(); // Create folder if not exist
    MatrixToImageWriter.writeToPath(bitMatrix, "PNG", qrFile.toPath());
}
private void fetchUser(String searchText) throws SQLException {
    DefaultTableModel model = (DefaultTableModel) userTable.getModel();
    model.setRowCount(0);

    java.sql.Connection con = dao.ConnectionProvider.getCon();
    java.sql.Statement st = con.createStatement();
    String query;

    if (searchText == null || searchText.trim().isEmpty()) {
        query = "SELECT * FROM userdetails";
    } else {
        query = "SELECT * FROM userdetails WHERE name LIKE '%" + searchText + "%' OR email LIKE '%" + searchText + "%'";
    }

        java.sql.ResultSet rs = st.executeQuery(query);
    while (rs.next()) {
        model.addRow(new Object[]{
            rs.getString("id"),
            rs.getString("name"),
            rs.getString("gender"),
            rs.getString("email"),
            rs.getString("contact"),
            rs.getString("address"),
            rs.getString("state"),
            rs.getString("country"),
            rs.getString("imagename")
        });
    }
}
