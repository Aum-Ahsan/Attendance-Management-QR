private void loadEmailsIntoTable() {
        try {
            Connection con = ConnectionProvider.getCon();
            java.sql.PreparedStatement ps = con.prepareStatement("SELECT email FROM userdetails");
            java.sql.ResultSet rs = ps.executeQuery();

            javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) userTable.getModel();
            model.setRowCount(0);

            while (rs.next()) {
                model.addRow(new Object[]{rs.getString("email")});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "\u274C Failed to load emails: " + e.getMessage());
        }
    }
