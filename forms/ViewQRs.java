private void fetchUser(String keyword) {
        try {
            Connection con = ConnectionProvider.getCon();
            java.sql.PreparedStatement ps = con.prepareStatement("SELECT email FROM userdetails WHERE email LIKE ?");
            ps.setString(1, "%" + keyword + "%");
            java.sql.ResultSet rs = ps.executeQuery();

            javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) userTable.getModel();
            model.setRowCount(0);

            while (rs.next()) {
                model.addRow(new Object[]{rs.getString("email")});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error during search: " + e.getMessage());
        }
    }
