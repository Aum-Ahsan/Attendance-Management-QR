    private void userTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_userTableMouseClicked

int selectedRow = userTable.getSelectedRow();
        if (selectedRow == -1) return;

        String email = userTable.getValueAt(selectedRow, 0).toString().trim();
        if (!email.isEmpty()) {
            String safeEmail = email.replaceAll("[^a-zA-Z0-9]", "_");
            String qrPath = "src/qrcodes/" + safeEmail + ".png";

            File qrFile = new File(qrPath);
            if (qrFile.exists()) {
                ImageIcon icon = new ImageIcon(qrPath);
                Image img = icon.getImage().getScaledInstance(lblimage.getWidth(), lblimage.getHeight(), Image.SCALE_SMOOTH);
                lblimage.setIcon(new ImageIcon(img));
            } else {
                JOptionPane.showMessageDialog(this, "\u26A0\uFE0F QR code not found for this user.");
                lblimage.setIcon(null);
            }
        }
    }//GEN-LAST:event_userTableMouseClicked
